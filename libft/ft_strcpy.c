#include "libft.h"

char	*ft_strcpy(char *dest, const char *src)
{
	char	*d;
	int		i;

	d = dest;
	i = 0;
	while (src[i] != '\0')
	{
		d[i] = src[i];
		i++;
	}
	d[i] = '\0';
	return (dest);
}
