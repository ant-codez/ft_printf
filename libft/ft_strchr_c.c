#include "libft.h"

char	ft_strchr_c(const char *str, int c)
{
	char	*s;

	s = (char *)str;
	while (*s != '\0')
	{
		if (*s == c)
			return (1);
		s++;
	}
	if (*s == '\0' && c == '\0')
		return (0);
	return (0);
}
